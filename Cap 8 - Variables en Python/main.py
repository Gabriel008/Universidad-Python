"""
Tipos de datos:

*Numeric:(*integer, *float, Complex Number)
Dictionary
*Boolean
Set
Sequense Type:(*String, List, Tuple)
"""
#Tipo int
x = 10
print(x)
print(type(x), '\n')  # class 'int'

#Tipo float
x= 14.5
print(x)
print(type(x), '\n')  # class 'int'

#Tipo String
x = "Hola Mundo"
print(x)
print(type(x), '\n')  # class 'str'

#Tipo Bool
x = True  # o False. Respetar mayusculas inicial
print(x)
print(type(x), '\n')  # class 'bool'
x = False
print(x)
print(type(x), '\n')  # class 'bool'





