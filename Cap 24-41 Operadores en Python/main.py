# Cap 29 - Operadores de Comparacion en Python

a = 4
b = 2


# resultado = (a == b)
resultado = a == b
print(f'Resultado == : {resultado}')

resultado = a != b
print(f'Resultado != : {resultado}')

resultado = a > b
print(f'Resultado > : {resultado}')

resultado = a >= b
print(f'Resultado >= : {resultado}')

resultado = a < b
print(f'Resultado < : {resultado}')

resultado = a <= b
print(f'Resultado <= : {resultado}')
